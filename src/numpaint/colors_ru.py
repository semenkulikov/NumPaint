    ("жёлтый", (0, 255, 255)),
    ("светло-жёлтый", (224, 255, 255)),
    ("золотистый", (0, 215, 255)),
    ("оранжевый", (0, 165, 255)),
    ("светло-оранжевый", (155, 210, 255)),
    ("персиковый", (180, 218, 255)),
    ("коралловый", (80, 127, 255)),
    ("красный", (0, 0, 255)),
